void _cq_split(unsigned char page, unsigned char addr, unsigned char incr, const unsigned char *data, unsigned int size) {
	int r = size%100;
	int sp = size/100;
	int count = 0;
	unsigned char tdata[104];

	dbg(DBG_LEV, " _cq_split r = %d, sp = %d, size = %d\r\n", r, sp, size);
	while(count < sp) {
		
		tdata[0] = page;
		tdata[1] = addr;
		tdata[2] = incr;
		tdata[3] = 100;
		memcpy(tdata+4, data+100*count, 100);

		cq_write_cmd(tdata);
		cq_flush();
		dbg(DBG_LEV, " split %d finished\r\n", count);
		
		count++;
	}
	
	tdata[0] = page;
	tdata[1] = addr;
	tdata[2] = incr;
	tdata[3] = r;
	memcpy(tdata+4, data+100*count, r);
	cq_write_cmd(tdata);
	cq_flush();
	
}

